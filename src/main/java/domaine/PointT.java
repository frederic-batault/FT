package domaine;

public class PointT {

	private double t;
	
	private double y;

	public double getT() {
		return t;
	}

	public void setT(double t) {
		this.t = t;
	}

	public double getY() {
		return y;
	}

	public void setY(double y) {
		this.y = y;
	}

	public PointT(double t, double y) {
		super();
		this.t = t;
		this.y = y;
	}

	public PointT() {
		super();
	}
	
	
}
